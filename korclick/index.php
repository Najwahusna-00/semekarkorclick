					Co-curricular activities also help a student concentrate more in the classroom and teach them valuable life lessons. 
					The importance of extracurricular activities at school has been proven by many types of research. Student should engage
					in at least two co-curricular activities to utilize their energy completely and explore their talents. 
					The co-curricular activities in SEMEKAR can be divided into three which are school uniform body, student
